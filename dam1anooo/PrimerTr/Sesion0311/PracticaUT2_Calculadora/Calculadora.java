package PrimerAnyo.PrimerTr.Sesion0311.PracticaUT2_Calculadora;

public class Calculadora {
    //suma
    public float suma(float valor1, float valor2) {
        float resultado;
        resultado = valor1+valor2;
        return resultado;
    }
    public float resta(float valor1, float valor2) {
        float resultado;
        resultado = valor1-valor2;
        return resultado;
    }
    public float multiplicacion(float valor1, float valor2) {
        float resultado;
        resultado = valor1*valor2;
        return resultado;
    }
    public float division(float valor1, float valor2) {
        float resultado;
        resultado = valor1/valor2;
        return resultado;
    }
}
