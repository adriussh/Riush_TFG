package PrimerAnyo.PrimerTr.Sesion1710.UT2EjerciciosBucles;

public class E1B {

    public static void main(String[] args) {

        int comienzo = 0;
        int termino = 100;
        System.out.println("Estos son los multiplos de 5 hasta 100");
        for (int multiplos=comienzo;multiplos<=termino;multiplos=multiplos+5)
        {
            System.out.println(multiplos);
        }
    }
}
